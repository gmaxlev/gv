<div id="box2" class="about"><h4 class="reverse">Фарбовані фасади</h4>
<div class="text">
Фарбовані кухні з МДФ. Щоб пофарбувати плити МДФ використовують емалеву фарбу. Головні переваги тут – багатющі можливості вибору декору фарбованих фасадів: 
колір - білий, чорний, металевий, будь якої яскравості, матовий, глянцевий, перламутр, комбінація кольору - стандартні, двоколірні, з переходом відтінків 
